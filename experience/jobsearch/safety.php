          <p>ePACK and other online job sites have made it easy for  you to find legitimate positions posted by legitimate employers.  At NC State, we screen every employer and  position listed in ePACK!  Unfortunately,  it is impossible to screen successfully 100% of the time -- when the same  technology can be used to create fraudulent opportunities.   </p>
          <p> Follow your instincts. If a position or job offer seems to be too  good to be true,  you feel uncomfortable with some of the information requested,  or something just doesn't seem right – contact the UCC for a second opinion.  </p>
          <p><strong>Watch for these  signs</strong></p>
          <ul>
            <li>Multiple misspellings in the job  description. </li>
            <li>Salaries that are way out of range. </li>
            <li>Postings that require an upfront  fee.   </li>
          </ul>
          <p><strong>Beware of red  flags </strong></p>
          <ul>
            <li>Request for payment of any kind. </li>
            <li>Request to transfer money. </li>
            <li>Request for credit card, bank or PayPal account  numbers.</li>
            <li>Receipt of an unexpectedly large check. </li>
            <li>Request for personal information such as your  social security number. </li>
            <li>Request to send a copy of your ID (i.e.,  driver's license to &quot;verify identity&quot;). </li>
            <li>Request to complete a background check <u>before</u> you can be considered for a position. </li>
            <li>Provide an email address that does not match the  company's website domain (i.e., <a href="mailto:jdoe@gmail.com">jdoe@gmail.com</a> rather than <a href="mailto:jdoe@companyname.com">jdoe@companyname.com</a>) </li>
            <li>A job description that focuses on the amount  of money&nbsp; you will make, not the job itself.</li>
            <li>Offer of a large payment/reward.</li>
            <li>Postings for &ldquo;mystery shoppers&rdquo;. </li>
          </ul>
          <p><strong>Handle immediately</strong></p>
          <ul>
            <li>Report your experience to the UCC, <a href="mailto:career_center@ncsu.edu">career_center@ncsu.edu</a> or 919.515.2396. Our goal is to protect you. </li>
            <li>End all communication with the employer.  </li>
            <li>If personal information was disclosed, monitor  your accounts for a few days, to be on the safe side.  Contact the police and report the fraud  or scam. </li>
            <li>If you have sent money to a fraudulent  employer, contact your bank or credit card company immediately to close your  account and dispute the charges. </li>
          </ul>
