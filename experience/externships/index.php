 <p><strong>Basic information</strong><u><br>
               </u>The University Career Center has developed the externship program to help students  build a successful future.&nbsp; Externships  are designed to provide students with an inside view of professionals at work,  and learn how to apply their major to a career field through job-shadowing.</p>
               <p>This  experience gives students the opportunity to connect with employers for  potential future internships and entry-level jobs.&nbsp; Students interact with valuable contacts and  gain the experience of speaking about their background and career aspirations  with practicing professionals.</p>
               <p><strong>When they occur </strong><br>
                 Externships occur each spring semester. &nbsp;Exact  dates are determined during fall semester. Externships will be either a one or  two day experience, based on the preference of each employer.</p>
               <p><strong>Who can participate</strong><u><br>
               </u>Currently  the program is offered for first year &amp; sophomore engineering students.  Eventually the program will expand to other majors. </p>
          <p><strong>Benefits to students</strong>               </p>
          <ul>
            <li>Show students the diverse opportunities  available in engineering through direct exposure to engineering environments. </li>
                 <li>Help students learn how to apply  their&nbsp;major through discussions with professionals who may have a similar  background. </li>
                 <li>Students can assess a "fit" for a  particular position, company, or industry.&nbsp; </li>
                 <li>Allow students to make career connections and  begin establishing networks for possible internships and future employment.</li>
                 <li><a href="/career/experience/externships/quotes.php">View quotes of past externs</a>.&nbsp; </li>
          </ul>
     <p><strong>Benefits to employers</strong></p>
               <ul>
                 <li>Gain  exposure and visibility for your company.</li>
                 <li>Introduces  you to potential interns and future employees. </li>
                 <li>Build  satisfying and lasting relationships (if choose to become a mentor). </li>
                 <li>Reconnects  you with NC State and the student experience. </li>
                 <li>Potentially  have students collaborate on research, laboratory work, data analysis or other  projects that will provide a learning opportunity while meeting a workplace  need. </li>
                 <li>It  is easy, flexible and informal. </li>
               </ul>
               <p><strong>History</strong><u><br>
               </u>Spring 2009 (pilot program):  9 students, 6 employers. <br>
               Spring 2010: 27 students, 19 employers.<br>
               Spring 2011:  58 students, 17 employers.
               </p>
