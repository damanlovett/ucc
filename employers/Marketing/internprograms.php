            <h4><a name="content" id="content"></a>Internship Program</h4>
            <table border="0" cellspacing="10" cellpadding="0" width="100%">
              <tr>
                <td valign="top"><p>Hiring students as interns is  one of the best ways to increase your visibility with NC State students, bring in fresh talent to work on important projects, and cultivate full-time hires for the future!</p>
                  <hr />
                  <p><span class="style2">Creating the internship </span><br />
                      Internships may be informal or very structured; there are no requirements from NC State. Successful programs possess  many of the following components:<br />
                      <br />
&bull;&nbsp; Challenging work assignments. <br />
&bull;&nbsp; Supportive work environment. <br />
&bull;&nbsp; Clearly stated expectations for student success. <br />
&bull;&nbsp; Active supervision and mentoring relationships. <br />
&bull;&nbsp; Opportunities to interact with other interns. <br />
&bull;&nbsp; Inclusion in meaningful events and meetings, when possible. <br />

                      <br />
                      <span class="style2">Advertising @ NC State </span><br />
 Use <a href="../Quicklinks/erecruiting.php">ePACK</a> to post your internship, search for resumes, or arrange an on-campus interview. Contact Leslie Bowman, <a href="mailto:leslie_bowman@ncsu.edu"> leslie_bowman@ncsu.edu</a> for assistance. </p>
                  <p><span class="style2">Timing</span> <br />
                      Recruiting begins    early fall semester   and continues through  early summer.&nbsp;<br />
                      <br />
                      <span class="style2">Determining the salary</span><br />
                      Intern salaries  range from $8.00 to 14.00 per hour (non-technical majors), $12.00-22.00 (technical majors).  Very few internships are unpaid.   The rate you choose will reflect your industry average as well as the    profile of the individual student.                      <br />
                      <br />
                      <span class="style2">Academic credit </span><br />
                    Most students do not earn academic credit for internships.</p>
                  <p>                    <span class="style2">Housing</span><br />
                    Students appreciate your    help if the internship is out-of-town -- corporate apartments, university housing,  suggested complexes, or pairing interns -- your help ensures a smooth transition.</p>
