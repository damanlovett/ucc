          <h1>PERSONAL STATEMENTS</h1>
          <p>A personal statement is a statement of your ideas
            and goals.  Some programs request one or
            two paragraphs, while others require longer essays in which you will write at
            length about your preparation and motivation for graduate study.  Before writing, consider what your reader
            might be looking for.  Study the program
            website. </p>
          <ul>
            <li> Get
                  started early, preferably during the summer before application deadlines.  A first draft can take a month to create. </li>
            <li> Review
                  advice from the <a
href="http://www.ncsu.edu/tutorial_center/writespeak/Admissions.htm">NC State Tutorial Center</a>. </li>
            <li>View <a href="http://owl.english.purdue.edu/owl/resource/642/02/">sample personal statements</a> and <a href="http://owl.english.purdue.edu/owl/resource/642/03/">advice from admissions representatives</a> [Purdue OWL]</li>
            <li> Have
                  your statement critiqued by faculty, <u><a
href="http://www.ncsu.edu/tutorial_center/writespeak/locations.htm">Tutor</a></u> or <a
href="http://www.ncsu.edu/career/quicklinks/staff.php">career counselor</a>. </li>
          </ul>
          <hr size=2 width="100%" align=center>
          <p>There are two main approaches to organizing an
            essay.  You can outline the points you
            want to cover and then expand on them, or you can put your ideas down on paper
            as they come to you, going over them, eliminating some, and moving others
            around until you achieve a logical sequence.  Making an outline will probably lead to a well-organized essay, whereas
            writing spontaneously may yield a more inspired piece of writing.  Use the approach you feel most comfortable
            with. </p>
          <p>If an institution requests autobiographical
            information, it does not have to be arranged chronologically. Your goal should be a clear, succinct
            statement showing that you have a definite sense of what you want to do and
            enthusiasm for the field of study.  Your
            essay should reflect your writing abilities, revealing the clarity, the focus,
            and the depth of your thinking. </p>
          <p>Admissions committees evaluate a number of things
                about you from your statement.</p>
          <ul>
            <li> Motivation
              and commitment to a field of study</li>
            <li> Interest
              in a particular institution</li>
            <li> Expectations
              of the program</li>
            <li> Writing
              ability</li>
            <li> Research
              or work experience</li>
            <li> Educational
              background</li>
            <li> Immediate
              and long-term goals</li>
            <li> Maturity</li>
            <li> Personal
              uniqueness - what you would add to the diversity of the class</li>
          </ul>
          <p>A critique is always helpful – especially from
            those writing recommendations and who are in your field.  They will know what to stress and what to
            keep brief.  Do not be surprised if you
            get differing opinions. In the end, only you can decide on the best way to
            present yourself. </p>
          <p>If you need to explain anything that could be
            construed as negative in your application (low grades or low test scores, for
            example), you can do this briefly on a separate sheet of paper titled
            &quot;Addendum&quot; which you will attach to the application.  Do not address this in the statement; keep
            the statement positive. </p>
