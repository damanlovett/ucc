          <p>You can avoid all of these!</p>
          <ul>
            <li> Arrive
              late</li>
            <li> Arrive
              unprepared – no resume, not sure what the company does</li>
            <li> Bring
              a friend, family member, or pet</li>
            <li> Chew
              gum</li>
            <li> Carry
              a beverage – no coffee, no water</li>
            <li> Allow
              your cell to ring or vibrate</li>
            <li> Dress
              down</li>
            <li> Wing
              the interview</li>
            <li> Have
              a weak or sweaty handshake</li>
            <li> Carry
              the scent of heavy perfume/cologne, tobacco, alcohol</li>
            <li> Interact
              unprofessionally or unkindly to any staff members</li>
            <li> Yawn</li>
            <li> Use
              "um," "like," or "you know" over and over</li>
            <li> Give
              a long answer hoping to "hit it right" </li>
            <li> Appear
              uninterested</li>
            <li> Unable
              to articulate your strengths</li>
            <li> Unable
              to connect your strengths to the job</li>
            <li> Say
              you don't have a weakness – identify one thing you'd like to improve</li>
            <li> Avoid
              eye contact – can appear untruthful or uninterested</li>
            <li> Make
              too much eye contact – can make someone uncomfortable </li>
            <li> Do
              not follow the interviewer's lead</li>
            <li> Be
              dishonest</li>
            <li> Make
              excuses</li>
            <li> Answer
              a question without thinking – a pause is ok</li>
            <li> Provide
              an answer, when you don't know the answer</li>
            <li> Take
              all the credit for the success of a group</li>
            <li> Speak
              negatively about past employers, colleagues, classmates, faculty</li>
            <li> Ask
              about salary</li>
            <li> When asked, &quot;Do you have any questions?&quot; -- you say no</li>
          </ul>
