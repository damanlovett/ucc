          <p> While most students prefer an internship during the summer  (or part-time during the semester) – you may have found one that requires you  to take a semester off.  Students at NC  State have done this before and successfully.   </p>
<p>To accept this opportunity – you will not &ldquo;withdraw&rdquo; from NC State, but  rather take a &ldquo;leave,&rdquo; as long as you are in good academic standing.  (This does not apply to students who are  awarded credit by their department or who accept a co-op.  In tthese cases, your status as a student remains unchanged). </p>
<hr />
<p><strong>Before the interview</strong></p>
                                <ul>
                                  <li><a href="http://www.ncsu.edu/advising_central/advisers.html">Academic Advisor</a>: discuss this option to be  sure the internship does not conflict with course sequence.  </li>
                                  <li><a href="http://www7.acs.ncsu.edu/financial_aid/contact.html">Scholarship Advisor</a>: discuss this option  to  learn if the internship impacts your scholarship; some donors require  notification in advance. </li>
                                  <li>Parents: discuss this option as  health insurance may or may not be affected.   </li>
          </ul>
                                <p><strong>Semester before the internship </strong></p>
          <ul type="square">
                                  <li>Do       not register for classes scheduled the semester you intend to intern.</li>
          </ul>
                                <p><strong>During the internship</strong></p>
                                <ul>
                                  <li>Complete the <a href="http://ncsu.edu/registrar/readmission/">readmission application</a> as soon as possible, but no later than 30 days prior to enrollment.  Cost is $50.</li>
                                  <li>Once readmitted, obtain your PIN and register for classes. </li>
                                  <li>Ensure continuation of financial aid; complete your FASFA by March 1.</li>
          </ul>
