            <h4><a name="content" id="content"></a>Success Stories  </h4>
            <p>Developing a successful recruiting program at NC State takes commitment, continuity, and creativity. Each year, new students arrive to campus who may not be aware of your  organization and  opportunities. We hope you find these insights  helpful as you develop your own success story at NC State!</p>
            <hr />
            <p><a href="http://www.kimley-horn.com/kha/">Kimley-Horn</a><br />
  The ties between Kimley-Horn and NC State go back a long way…40 years to be exact. Kimley-Horn was founded in 1967 by an NC State professor (Bill Horn) and an NC State grad (Bob Kimley). And we have been tightly tied to the university ever since. Today, we work closely with Career Services as we search for the most qualified civil engineers, environmental engineers, and environmental scientists to join our growing firm. </p>
            <p>Approximately 130 graduates of the university work at Kimley-Horn today. Several began work at Kimley-Horn as interns. Internships and co-ops allow students to get to know our firm as well as develop real-world, real work experience that provides an essential knowledge base on which to build their career. Competition is tough for top candidates so we bring our most recent recruits with us to career fairs and information sessions to chat with students and answer all their questions. </p>
            <p>Our firm and the university have been linked for four decades. We look forward to the decades to come.<br />
              <strong>----- </strong><strong>Meghan Pike,&nbsp; Human Resources</strong> </p>
            <hr />
            <p><a href="http://www.northropgrumman.com/">Northrop Grumman</a><br />
Embracing diversity and getting involved on campus is&nbsp;a&nbsp;key&nbsp;to&nbsp;the&nbsp;great&nbsp;success&nbsp;Northrop&nbsp; Grumman&nbsp;has&nbsp;experienced&nbsp;over&nbsp;the&nbsp;past ten&nbsp;years.&nbsp;&nbsp;The&nbsp;successful&nbsp;recruiting&nbsp;of&nbsp;diversity candidates at&nbsp;N.C. State&nbsp;has&nbsp;been one of our most prized accomplishments&nbsp;during my involvement&nbsp;these past six years.&nbsp; </p>
            <p>The first time I showed up for interviews at N.C. State, we had one interview scheduled.&nbsp; We were new on campus and had done nothing to meet the students, the faculty, the staff, and the Career Center employees ahead of time.&nbsp; With the help of the now retired Director and the current Director of the Career Center, Ms. Carol Schroeder, I was introduced to the many diverse&nbsp;ways&nbsp;to meet with&nbsp;and attract&nbsp;outstanding engineering talent for Northrop Grumman.&nbsp;&nbsp;</p>
            <p>After two years of hard work and many visits on campus,&nbsp;we hired 35 new graduate engineers&nbsp;for our Baltimore location in one year.&nbsp; We began a search for a broad range of degrees and experiences and have now hired more than 75 engineers for our Electronic Systems Sector headquarters in Baltimore, and supported many co-op students&nbsp;at our Newport News facility in Norfolk, Virginia.&nbsp; Our diversity numbers include 33% women, and 52% women and minorities over the last six years&nbsp;at State.&nbsp; However the true diversity is in the degrees, experiences, and ideas generated by the students graduating&nbsp;from N.C. State.&nbsp; From that first year, where we failed to hire that one software candidate, our hiring success at N.C. State has included BS, MS and PhD candidates who are currently leading technology breakthroughs&nbsp;for Northrop Grumman.&nbsp; We have also hired graduates from several different degrees and experiences; Mechanical &amp; Aerospace Engineering, Industrial &amp; Systems Engineering, Operations Research Engineering, Computer Engineering, Electrical Engineering, Physics, Software Engineering, and Material Science &amp; Engineering.<br />
                <strong>----- Eric P. Pearson, Director, Engineering Staff Development</strong></p>
            <hr />
            <p><a href="http://www.tindallcorp.com/">Tindall Corporation</a><br />
  How do you attract the best engineers if you&rsquo;re a small company competing  against the Fortune 500? You recruit the same way that they do. We spend the  time on developing relationships with key faculty members. We conduct  information sessions for appropriate student organizations. We attend job  fairs. You work very hard in developing relationships with Career Services.  And, you target the best students.<br />
              Since we began to strengthen our relationship with NC State  early in 2003, we have been able to hire five quality individuals who are all  making significant contributions early in their careers. Five may seem to be an  insignificant number to many companies, but it represents a huge success for  us. Thanks to our friends in Career Services for giving us the same quality  attention that they give to the biggest and best known corporations. We feel  that the best is yet to come.                <br />
                <strong>----- Daniel E. Faucette, 
              Director of Industrial Engineering</strong></p>
            <hr />
            <p><a href="http://www.lowes.com/">Lowe's Companies</a><br />
              As Lowe's continues to grow, so does our relationship with NC State
              University. Lowe's has a history of hiring NCSU students into both
              summer internships and full-time positions at our corporate offices,<br />
              distribution centers, and in our stores. In particular, we have had
              tremendous success recruiting undergraduate business students into our
              Distribution Management Training Program. These students are well on
              their way to becoming future leaders within Lowe's distribution network.
              Additionally, Lowe's continues the pursuit of talented Industrial
              Engineering majors to start careers in our customer support center as
              well as throughout our Distribution network.
              Whether we are on campus for career fairs, classroom sessions,
              information sessions or full-time job interviews, our team is always
              looking for the next NCSU graduate who is ready to begin building their
              career with us!<br />
              <strong>----- </strong><strong>Sarah Cook, College Recruiter</strong></p>
            <hr />
            <p><a href="http://www.ibm.com/">IBM</a><br />
              A quick drive from the NC State campus is the IBM site in 
              Research Triangle Park, the company's largest facility, which is situated 
              on more than 800 acres and occupies over seven million square feet of 
              space. The IBM site is a hub for software development, with key labs in 
              the area that accommodate the largest and most diverse programming skill 
              base in the industry.</p>
            <p>              Over the years, the relationship between IBM and NC State has  evolved into 
              a great partnership that creates a positive recruiting experience for both 
              the students and the business. The university has consistently provided 
              some of the brightest and most technically prepared students that have 
              joined IBM. There are numerous recruiting activities that IBM participates 
              in on campus, including one-on-one interviewing, participation in career 
              fairs, speaking to student groups, working with students in resume 
              workshops and mock interviews, as well as engaging in panel discussions.              </p>
            <p>IBM hosts its premier recruiting event, the IBM Corporate  Day, on the NC 
              State campus twice a year. This event is like a mini IBM-only career fair 
              that gives students the opportunity to meet, mingle and discuss full time, 
              co-op and summer intern options with managers and alumni from across IBM's 
              business units.              <br />
              <strong>----- Traci A. Reston, 
              University Recruiting and Relations Manager</strong></p>
            <hr />
            <p>We look forward to helping you develop a successful campus recruiting program at NC State!<br />
              Consult with the <a href="../Quicklinks/staff.php">career counselor</a> who works with targeted majors for assistance. </p>
