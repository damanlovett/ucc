<h4><a name="content" id="content"></a>Our Services<strong>&nbsp;</strong></h4>
<p>  <strong>Drop-ins</strong>&nbsp; <br>
  Have a quick question? Meet with the Career Counselor on duty  Monday through Friday from 11:00am-2:00pm. No appointment is necessary.</p>
<p><strong>Individual appointments</strong><br />
Need a little more time? Make an appointment to meet with the <a href="/career/students/quicklinks/staff.php">career counselor</a> who works with your major. Call 515.2396 to schedule an appointment</p>
<p><strong>Career path checklist </strong><br>
  Use the <a href="../students/explore/checklist.php">checklist</a> to stay on track with your career development @ NC State -- as a freshman, sophomore, junior and senior.</p>
<p><strong>Career fairs</strong><br>
  Attend  <a href="/career/students/quicklinks/careerfairs.php">career fairs</a> each semester to interact with employers who hire NC State students.</p>
<p><strong>Job &amp; internship guide</strong><br> 
Pick up a 
<a href="../jobinternguide/index.php">job &amp; internship guide</a>, an indispensable resource providing structure and support for your search.</p>
<p><strong>ePACK</strong> <br>
  Use 
  <a href="../students/jobsinternships/epack.php">ePACK</a>, NC State's official  on-line recruiting system. Apply for internships and jobs, sign  up for on-campus interviews, find information sessions, have your resume searched by employers, and  obtain employer contacts. </p>
<p><strong>On-campus interviews</strong><br />
  Participate in <a href="../students/interviewing/campus.php">on-campus interviews</a> for internships and jobs, beginning early fall semester and continuing through the spring.&nbsp; </p>
<p><strong>Employer information sessions </strong><br />
  Meet employers at information sessions scheduled each semester. All students are invited to attend. Sessions are posted in <a href="../students/jobsinternships/epack.php">ePACK</a>.</p>
<p><strong>Workshops &amp; presentations</strong><br />
Look for UCC <a href="../students/quicklinks/workshops.php">workshops and presentations</a> on campus each semester. Invite us to present to your group! </p>
<p><strong>Podcasts</strong> <br>
  Listen to <a href="/career/careertalk/index.php">career talk</a>, brief interviews with students, employers, career ambassadors and career counselors on a variety of topics. </p>
<p><strong>Confidential credential file services</strong><br>
  Use the  confidential <a href="../students/resumesletters/credential.php">credential file</a> services if you are  seeking positions in teaching, student personnel services, or administration at all educational institutions (ie., K-12 &amp; higher education institutions). Another option, particularly for graduate students, is <a href="http://www.interfolio.com/index.cfm">Interfolio</a>, a secure web-based tool that allows you to store, manage and send electronic and print files.  </p>
<p>    <strong>Career  library</strong><br />
    Visit the career library  in 2100 Pullen Hall to find  a variety of publications on majors and careers,  job search preparation, and graduate school. Use our wireless internet access or computer workstations. </p>
<p><strong>Students with special issues</strong><br>
  Find additional advice for students who face <a href="../students/quicklinks/issues.php">special issues</a> or unique challenges to their search. </p>
<p><strong>Alumni services</strong><br />
  Continue to use our services for one full semester after graduation. Transition to the Alumni Association's <a href="http://www.alumni.ncsu.edu/careerservices/">career services</a> after that time. </p>
<hr />
<p>Our services are available to all NC State students in degree programs, including distance education degree programs. 
