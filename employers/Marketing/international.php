            <h4><a name="content" id="content"></a>Hiring International Students </h4>
            <p>Most international students at NC State are in F-1 of J-1  visa status.&nbsp; Hiring these students may be possible and an option for your organization to fill specific internship needs.&nbsp; &nbsp; </p>
            <p><span class="style1">Curricular practical training (CPT)</span><br />
              F-1 and J-1 student visa holders may be eligible to work in the US as an  intern/co-op in order to gain work experience in their fields of study prior to  graduation.&nbsp; Students must meet certain eligibility requirements and obtain their academic advisor's recommendation. These internships are  officially titled co-ops at NC State since they will be documented carefully in  order to qualify for CPT. The Office of International Services at NC State can authorize CPT and no additional permission from Immigration (USCIS) is necessary.  Hundreds of NC State international students engage in a co-op each year - please contact one of the offices below for more information. <br />
  <a href="http://www.ncsu.edu/co-op_ed/">Cooperative  Education</a> / <a href="http://www.ncsu.edu/ois/">Office  of International Services</a></p>
            <p><span class="style1">Optional practical training (OPT)</span><br />
              F-1 student visa holders may also apply to USCIS for off-campus work authorization (OPT) to work  in their field of study in the U.S. after graduation for 12 months.&nbsp; After this time, employers may have the option to  sponsor an international student for an employment visa - consult your HR department or an immigration attorney for more information.&nbsp;<br /> 
              <a href="http://www.uscis.gov/portal/site/uscis">U.S.  Citizenship &amp; Immigration Services</a></p>
            <p><span class="style1">Long term employment</span><br />
              Most employers who wish to hire international students (or graduates) for permanent or long-term positions (or when the student's 12 months of OPT will expire) work with an immigration attorney or in-house counsel to petition the USCIS for an H-1B Specialty Occupation visa. International graduates may be eligible for up to an additional six years of  employment in the U.S. with your firm.&nbsp;   </p>
            <p>Many international students intend to return to their home  countries after having established themselves professionally with your  firm.&nbsp; In such cases, the more expensive  and time consuming U.S. permanent resident or &ldquo;green card&rdquo; process is not  necessary.</p>
