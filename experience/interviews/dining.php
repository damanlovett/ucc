   <p>If you are invited to interview during a meal, you want to feel  comfortable and make a good first impression.   The rules of dining etiquette are fairly straightforward and mostly  require common sense. </p>
   <hr />
   <p><strong>Using  your napkin</strong><br>
     As soon as you are seated, place your napkin in your lap.  Sometimes, at very formal restaurants, the  waiter may do this for you.  Let the  napkin do its job – catch food that drops in your lap or dab crumbs from your  lips.  It should not be shaken out,  crumpled up, or used as a hankerchief.   If you get up from the table, leave your napkin to the left of your  plate.  </p>
   <p><strong>Ordering<br>
   </strong>Skim the  menu quickly.  Choose a medium-priced  item.  If you have difficulty deciding, ask the server or your host for suggestions. Do not discuss dietary restrictions.  Select an item that is easy  to eat (no bones).   We suggest choosing a safe beverage such as  soda, juice, or water.  At all times, be appreciative of restaurant staff.    </p>
   <p><strong>Participating in conversation<br>
   </strong>Be prepared  to contribute to the conversation and include everyone at  the table.  This is an opportunity for  the employer to get to know you in a more casual setting.  Stay focused on the conversation despite  distractions.  Relax but do not let your guard down. This is still an interview.  Avoid controversial subjects.  Brush up on  current news as this can be a conversation starter.    </p>
   <p><strong>Handling  the table setting</strong>  <br>
     Most table settings are simple and can be easily figured out. It's a bit more confusing when you're faced with multiple utensils. Keep this general rule in mind --  &ldquo;work from the outside in.&quot;   Salad served first?  Use the outside fork to the left. View sample <a href="http://didyouknow.org/tablesettings/">formal table setting</a>.</p>
   <p><strong>Being served<br>
   </strong>Wait for everyone at your table to be served before beginning to eat,  unless you are encouraged to begin.  Eat  slowly while waiting for their food to be served.  When all are served, make every attempt to  eat at the same pace as others.   </p>
   <p><strong>Using  utensils</strong><br>
     Cut one bite at a time then rest your knife.  Between bites, your utensils should rest on  the edge of your plate.  Your knife rests  on the back of your plate, your fork on the side of your plate.  When finished, rest both together  horizontally across the center of your plate.  </p>
   <p><strong>Eating &amp; talking <br>
   </strong>The main point of the meal is to interact; eating is secondary.  Take very small bites so you can eat and  answer questions.  Never speak with food  in your mouth.  </p>
   <p><strong>Finishing  up</strong><br>
     When finished with a course, leave your plate in its original  location.  Do not move your plates or  stack them.  Do not use a toothpick, even  if one is offered.  When ready to leave,  place your napkin to the left of your plate and push in your chair.  Thank the employer!</p>
   <hr />
   <p>Though there are many things to keep in mind while dining, common sense  should be your guide.  When in   doubt, watch your host and follow their cues!</p>
