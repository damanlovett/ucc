  <h4><a name="content" id="content"></a>Visiting NC State</h4>
  <p><span class="style4">Parking</span><br />
    You may park in the <a href="http://www.ncsu.edu/facilities/buildings/danallendeck.html">Dan Allen Parking Deck</a> located on Dan Allen Drive. Visitor permits are required in the C and DD zones and can be picked up at the following locations: </p>
  <ul>
    <li>2100 Pullen Hall. Park temporarily in the metered lot at the corner of Dan Allen Drive and Thurman Drive. Cost - $0. </li>
    <li> <a href="http://www2.acs.ncsu.edu/trans/maps/download/MCampus_Visitor_map.pdf">Visitor Information Booth</a> located on Stinson Drive. Cost - $2.00 per day. </li>
  </ul>
  <p>You may also park in the Dan Allen Parking Deck's <u>pay lot</u>. Rate - $2.00 for the first hour, $1.00 for each consecutive hour, $6.00 daily maximum. Cash is not accepted&nbsp;but the following credit cards may be used: American Express, Discover, Master Card, or Visa. <em>Note: Visitor permits CANNOT be used in the pay lot. </em> </p>
  <hr />
  <p><span class="style4">Directions</span><br />
  From RDU airport:&nbsp; <br />
  Rte. 40E to Wade    Ave.&nbsp; Follow  Wade Ave. into Raleigh and turn right (at the 4th light) on Dixie Trail.&nbsp; Go to the end and turn  left on Hillsborough St. Turn right at first light,  Dan Allen Dr.&nbsp; If you have a parking permit, turn right into  Dan Allen Parking Deck just before the bridge.&nbsp; Pullen Hall is the first building on your left after the bridge.&nbsp; </p>
  <p>From 440 beltline:&nbsp; <br />
    Exit Hillsborough    St. and drive east toward downtown Raleigh.&nbsp; Pass Meredith College on your left and  continue toward NC State. Turn right  on Dan Allen Dr.&nbsp; If you have a parking permit, turn right into  Dan Allen Parking Deck just before the bridge.&nbsp;  Pullen Hall is the first building on your left after the bridge.&nbsp;</p>
