          <h4><a name="content" id="content"></a>Personal Statements</h4>
          <p>A personal statement (a.k.a. statement of purpose and  application essay) is essentially a statement of your ideas and goals. </p>
          <p>Requirements vary. Some programs request only one or two  paragraphs about why you want to pursue graduate study, while others require  longer essays in which you will write at length about your motivation for  graduate study, your strengths and weaknesses, your greatest achievements, and  solutions to hypothetical problems. </p>
          <p>Before writing, consider what your reader might be looking  for. You may learn this through the application itself, discussions you have  had with program representatives, and the program website. </p>
          <p>Admissions committees evaluate a number of things about you  from your statement.</p>
          <p>-&nbsp; Motivation and  commitment to a field of study.<br />
            -&nbsp; Expectations of the  program.<br />
            -&nbsp; Writing ability.<br />
            -&nbsp; Research or work  experience.<br />
            -&nbsp; Educational  background.<br />
            -&nbsp; Immediate and  long-term goals.<br />
            -&nbsp; Reasons for  deciding to pursue graduate study at a particular institution.<br />
            -&nbsp; Maturity.<br />
            -&nbsp; Personal uniqueness  - what you would add to the diversity of the entering class.</p>
          <p>There are two main approaches to organizing an essay. You  can outline the points you want to cover and then expand on them, or you can  put your ideas down on paper as they come to you, going over them, eliminating  some, and moving others around until you achieve a logical sequence. Making an  outline will probably lead to a well-organized essay, whereas writing  spontaneously may yield a more inspired piece of writing. Use the approach you  feel most comfortable with. </p>
          <p>Whichever approach you use, you will want someone to  critique your essay. Your advisor and those who write your letters of  recommendation may be very helpful. If they are in your field, they will be  able to tell you what to stress and what to keep brief. Do not be surprised,  however, if you get differing opinions. In the end, only you can decide on the  best way to present yourself. </p>
          <p>If an institution requests autobiographical information, it  does not have to be arranged chronologically. Your goal should be a clear,  succinct statement showing that you have a definite sense of what you want to  do and enthusiasm for the field of study. Your essay should reflect your  writing abilities, revealing the clarity, the focus, and the depth of your  thinking.</p>
          <p>If you need to explain anything that could be construed as  negative in your application (low grades or low test scores, for example), you  can do this on a separate sheet of paper titled &quot;Addendum&quot; which you  will attach to the application. Be brief and to the point. Do not address this  in the statement; keep the statement positive.</p>
          <hr />
            <p>&hellip; an excerpt from Peterson's Graduate &amp; Professional  Schools guide - with permission including restricted access.</p>
            <p>Interested in having your personal statement critiqued by a writing expert? Use the <a href="http://www.ncsu.edu/tutorial_center/writespeak/locations.htm">Writing and Speaking Tutorial Services</a> offered at NC State. </p>
