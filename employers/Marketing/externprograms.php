            <h4><a name="content" id="content"></a>Externship Program</h4>
            <table border="0" cellspacing="10" cellpadding="0" width="100%">
              <tr>
                <td valign="top"><p><span class="style2">Basic Information </span><br />
The University Career Center has developed the Externship program to help students build a successful future. Externships are designed to provide students with an inside view of professionals at work, and learn how to apply their major to a career field through job-shadowing. </p>
                  <p>This experience gives students the opportunity to connect with employers for potential future internships and full-time jobs. Students interact with valuable contacts and gain the experience of speaking about their background and career aspirations with practicing professionals.</p>
                  <p><a href="../../pdfs/extern_student.pdf">Student FAQs</a><br />
                    <a href="../../pdfs/extern_employer.pdf">Employer FAQs</a></p>
                  <hr />
                  <p><span class="style2">When does it occur </span><br />
  Externships occur each spring semester. Exact dates are determined during Fall semester. Externships will be either a one or two day experience, based on the preference of each employer. </p>
                  <p><span class="style2">Who can participate </span><br />
  Currently the program is offered for First Year &amp; Sophomore Engineering students. Eventually the program will expand to other majors. </p>
                  <p class="style2">Benefits to students                  </p>
                  <p>&#149;&nbsp; Show students the diverse opportunities available in engineering through direct exposure to engineering environments. <br />
                    &#149;&nbsp; Help students learn how to apply their&nbsp;major through discussions with professionals who may have a similar background.<br />
                    &#149;&nbsp; Students can assess a &quot;fit&quot; for a particular position, company, or industry. <br />
                    &#149;&nbsp; Allow students to make career connections and begin establishing networks for possible internships and future employment. <br />
                    &#149;&nbsp; View quotes of past externs. (link) </p>
                  <p class="style2">Benefits to employers </p>
                  <p>&#149;&nbsp; Gain exposure and visibility for your company. <br />
                    &#149;&nbsp; Introduces you to potential interns and future employees. <br />
                    &#149;&nbsp; Build satisfying and lasting relationships (if choose to become a mentor). <br />
                    &#149;&nbsp; Reconnects you with NC State and the student experience. <br />
                    &#149;&nbsp; Potentially have students collaborate on research, laboratory work, data analysis or other projects that will provide a learning opportunity while meeting a workplace need <br />
                    &#149;&nbsp; It is easy, flexible and informal. </p>
                  <p><span class="style2">History </span><br />
  Pilot program occurred at the end of Spring 2009 semester exams. We had 9 student participants and 6 employers. Spring 2010 Externship program occurred during spring break week with 27 students and 19 employers. </p>
                  <hr />                  <p><span class="style2">Questions </span><br />
