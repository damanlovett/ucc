           <div id="tab-2"> 
          <p>Questions follow the STAR technique and are based upon the notion that past performance is a good indicator of future performance.   <strong>How to prepare:</strong>  identify 2-3 challenging situations that you will draw from. These situations  can be from academics, work, or community experiences. </p>
           <p>
            <strong>S</strong> ------ situation (give an example of a situation) <br>
            <strong>T</strong> ------ task (describe the tasks involved) <br>
            <strong>A</strong> ------ action (explain
            how you responded)<br>
            <strong>R</strong> ------ results (describe the positive outcome or result)</p>
           <ul><li> Give me a specific example of a time when you used good judgment and logic in solving a problem. </li>
             <li>Give me an example of a time when you showed initiative and took the lead. </li>
             <li> Tell me about a recent situation in which you had to deal with an upset customer or co-worker.&nbsp; </li>
             <li> Give me an example of a time when you motivated others. </li>
             <li> Tell me about a time when you were forced to make an unpopular decision. </li>
             <li> Give me an example of a time when you had to conform to a policy which you did not agree. </li>
             <li>Tell me about a time when you had to go above and beyond the call of duty to get a job done. </li>
             <li> Give me an example of a time when you had to make a split second decision. </li>
             <li> What is your typical way of dealing with conflict?&nbsp; Give me an example. </li>
             <li> Tell me about a difficult decision you have made in the last year. </li>
             <li> Give me an example of a time when something you tried to accomplish failed.&nbsp; </li>
             <li> Give me an example of a time when you set a goal and were able to meet or achieve that goal. </li>
             <li>Describe a time when you anticipated potential problems and developed preventive measures. </li>
             <li>Tell me about a time when you had to use your presentation skills to influence someone's opinion.&nbsp;</li>
             <li> Describe a time when you set your sights too high (or too low). </li>
             <li> Give me an example of the most creative project you have worked on. </li>
