	<div id="tabs-1">
		<ul>
            <li> 1 page
              (1-2 pages for graduate students) </li>
            <li> Professional
              in appearance </li>
            <li> White or
              off-white paper</li>
            <li> 10-12 pt.
              font </li>
            <li> ¾-1"
              margins </li>
            <li> Clear,
              concise, honest, well-organized </li>
            <li> Prioritize
              categories (after &quot;Objective&quot; and &quot;Education&quot;) </li>
            <li> Focus on
              accomplishments, skills, interests </li>
            <li> Help the
              reader understand what you have to offer </li>
            <li> Draw from
              all parts of your life - academics, work, community, projects, interests </li>
            <li> Include
              significant experiences from H.S. (they are not off limits) </li>
            <li> Convey
              personal characteristics: energy and enthusiasm, flexibility and adaptability,
              capacity for teamwork, leadership potential, honesty and integrity </li>
            <li> Error
              free – read aloud to check for errors</li>
            <li> What
              about GPA?  (<a
href="/career/pdfs/gpa.pdf">Low GPA</a> / <a
href="/career/pdfs/gpa-no.pdf">no GPA</a>)</li>
            <li> Have your
              resume reviewed </li>
            <li> Upload
              into ePACK </li>
            <li> Customize
              for individual readers (when advantageous) </li>
            <li> Update
              each semester </li>
          </ul>
	</div>
	<div id="tabs-2">
		<ul>
            <li> Don't leave the employer guessing what you have to offer </li>
            <li> Resume
              templates </li>
            <li> Attach
              photos</li>
            <li> Personal
              info (date of birth, height, weight) </li>
            <li> &lt;10
              pt. font </li>
            <li> Poor
              organization </li>
            <li> Typos or
              grammatical errors </li>
            <li> Use an
              objective that does not fit the position </li>
            <li> List
              every course taken (choose selectively) </li>
            <li> Underestimate
              your experiences or activities (paid or unpaid) </li>
            <li> Assume
              interests and hobbies don't matter </li>
            <li> Use
              personal pronouns – I, my, me </li>
            <li> Emphasize
              skills you do not wish to use </li>
            <li> Pay a
              resume service (the UCC is free!) </li>
          </ul>
