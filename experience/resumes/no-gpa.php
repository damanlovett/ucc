          <p> As an incoming freshman, transfer, or graduate student – your GPA in  Registration &amp; Records is 0.0 until completion of your first semester.  You undoubtedly have a few questions. </p>
          <ul>
            <li> How do I address GPA on my resume?  </li>
            <li>How will I qualify to apply for jobs or interviews in  ePACK?</li>
          </ul>
          <hr />
          <p><strong>On your  resume</strong> <br>
We suggest that you leave GPA blank and not estimate, since it cannot  be backed up by transcript.  Instead,  address your current performance in an email or cover letter.  Be sure to include GPA from other  institutions on your resume -- H.S.,  transfer, or previous degree.  This gives  the reader a good idea of your potential at NC State.</p>
          <p>If you decide to estimate your GPA, because you are performing well and  expect to continue – you will do so at your own risk. If you accept a job then find your  actual GPA is lower than previously stated, an offer can be rescinded by the  employer.  <strong><em>    </em></strong></p>
          <p><strong>In  ePACK</strong> <br>
          If you are unable to apply for jobs or interviews in ePACK because of a GPA requirement, we have two suggestions.</p>
          <ul>
            <li>When employer contact information<strong><em> is available</em></strong> in ePACK -- send an email and state your interest in the opportunity.   Attach a copy of your resume.  This  leaves the decision in the hands of the employer!  </li>
            <li>When employer contact information<strong> <em>is not available</em></strong> in ePACK -- consult   your <a href="../../about/staff.php">career counselor</a> to discuss the best option available.  We may forward your resume directly to the  employer or apply you manually through the ePACK system.</li>
          </ul>
