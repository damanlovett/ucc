        <p>Preparation and participation are keys to your success.  No one said this search was easy.  In fact, it’s likely to take more time and  creativity than you anticipate.  Work at  it one step at a time … over time.   Commit yourself to the process and remember, we are here to help.     </p>
<p><strong>Preparation</strong></p>
        <ul>
          <li>Start  a job search notebook or spreadsheet to track all activity with employers.  </li>
          <li>Identify  personal strengths, skills, and interests.   You&rsquo;ll communicate these to an employer through your job search  correspondence and during an interview.  </li>
          <li>Create  an outstanding resume and cover letter.   Have them critiqued by UCC staff or a professional of your choice.      </li>
          <li>Create  your ePACK account, <a href="http://www.ncsu.edu/epack">www.ncsu.edu/epack</a>.<br>
            Employers use ePACK to hire for  internships &amp; entry-level jobs. 
            <ul>
              <li>View the       &ldquo;quick start guide.&rdquo; </li>
              <li>Create or       update your profile.  </li>
              <li>Upload your up-to-date       resume. </li>
              <li>Set up a &ldquo;job       search agent.&rdquo;</li>
              </ul>
            </li>
          <li>Protect  your on-line presence by being selective about photos and postings for possible  outside viewing.  Secure your sites with  appropriate privacy settings.  </li>
          
          <li>Research  potential employers; know basic products/services and locations before applying  or interviewing.    </li>
          <li>Prepare  for interviews.  </li>
          <li>Review sample       interview questions and develop strong answers.  </li>
          <li>Prepare       questions to ask employers.</li>
          <li>Assemble  supporting documents – unofficial transcript (all students), list of references  (graduating students), work sample of a project (if appropriate for your field).   </li>
          <li>Select  professional interview attire.</li>
          <li>Purchase  a professional portfolio (or use a simple dark color folder) to carry your job  search documents to an interview or career fair.</li>
          </ul>
        <p><strong>Participation</strong></p>
        <ul>
          <li>Use  multiple strategies simultaneously since employers recruit in different ways!</li>
          <li>Check ePACK       regularly.  <br>
            Create a job       agent and opt for email notification of matches
            <ul>
              <li>Apply for       posted internships &amp; entry-level jobs.</li>
              <li>Apply for       on-campus interviews. </li>
              <li>Attend       employer information sessions.</li>
              <li>Use the       employer directory; contact employers on your own.     </li>
            </ul>
            </li>
          
          <li>Attend  career fairs -- these employers hire NC State students!  </li>
          <li>Attend  company presentations arranged through your department and student  organizations. </li>
          <li>Let faculty,  classmates, family, friends, and co-workers know about your search; they may  have valuable advice, leads and suggestions.     </li>
          <li>Use  resources and contacts available through professional and honor societies as  well as social organizations. </li>
          <li>Use the  helpful job search resources and strategies found on the UCC website.  Do not wait for positions to be posted;  contact employers of interest directly in person, by email, phone or US mail.</li>
          <li>Apply  directly through company websites; it is often required by employers.  </li>
          <li>Email  thank you notes within 24 hours of interviews or assistance you receive from  others.</li>
          </ul>
