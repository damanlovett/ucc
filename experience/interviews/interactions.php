   <p> Professional interactions are based on good manners and common sense.  When interacting with employers during career  fairs, interviews, information sessions, meals, and social situations, you want  to feel comfortable and confident that you are making a positive impression.  It reveals to employers how you  will interact in the future with colleagues, clients, and customers.  </p>
   <hr />
   <p><strong>Preparation</strong><br>
     You want your first impression to be positive.  Ask in advance about proper attire for the  occasion.  Err on the side of being more  conservative.  Attend to proper personal  grooming.  Arrive to the event with  purpose, confidence, and a smile.   Silence your cell phone.</p>
<p><strong>Punctuality<br>
</strong>Be on time.  Always allow extra  time - prepare to arrive 10-15 minutes early.   If you encounter an unforeseen delay, let your contact know in  advance if possible. </p>
<p><strong>Body language<br>
</strong>First impressions are made before you begin to speak.  Smile, use good posture, and move with  confidence.  Convey energy and  enthusiasm.  In a group, focus in on the  conversation at hand and make good eye contact.   Avoid scanning the room for the next opportunity.  Keep your hands by your side to avoid  fidgeting.  </p>
<p><strong>Introductions<br>
</strong>Rise if you are seated, smile, shake hands, make eye contact, and repeat  the other person’s name.  Give the same  firm handshake to a woman as you would to a man. Know how you will introduce yourself -- develop  <a href="intro.php"> your introduction</a>.  When introducing others, use full names and explain who people are to each other. Introduce a younger  to an older person (or junior  to more senior level employee). </p>
   <p><strong>Mixing &amp; mingling</strong><strong> </strong><br>
     It is not  always easy to socialize in a room of strangers.  Start by approaching someone who is standing  alone.  Briefly introduce yourself.  Be friendly and sincerely interested in the  conversation.  Look for what you have in  common.  Steer clear of controversial  topics.  For events where a host is present, greet this individual within the first  15 minutes and be sure to say thank you before you leave.</p>
   <hr />
   <p>Your goal is  to treat everyone with equal courtesy and respect.  Developing the art of business etiquette will  help you build productive professional relationships now and in the  future.  </p>
