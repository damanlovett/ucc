          <p> Most employers prefer to see a GPA on your resume.  While your GPA may not be as strong as you  want it to be -- you must find a way to present it.  It is your decision as to whether you note it  on your resume -- but you'll likely have to answer the question during an  interview.  <strong></strong></p>
          <p>            Here are ways to present your GPA <em><strong>in the best light!</strong></em><strong></strong></p>
          <ul>
            <li><strong>2.9/4.0 major GPA, 2.4/4.0 overall GPA</strong><br>
              Place greater emphasis on your major GPA.<br>
              <br>
            </li>
            <li><strong>GPA:&nbsp; 3.1/4.0 since fall 2010, 2.6/4.0 overall</strong><br>
            Show that you are improving your grades and doing so in higher level classes.<br>
            <br>
            </li>
            <li><strong>2.86/4.00 GPA</strong><br>
              List GPAs below 3.0 (2.7-3.0) in highly competitive majors.  <br>
              <br>
            </li>
            <li><strong>3.2/4.0 projects/labs, 2.5/4.0 overall</strong><br>
              Emphasize success in hands-on application of coursework in your major.<br>
              <br>
            </li>
            <li><strong>2.6/4.0 GPA overall, worked 20 hrs/week while in school</strong><br>
              Let the employer know about additional demands on your schedule.  <br>
              <br>
            </li>
            <li><strong>Financially responsible for 100% of college expenses.</strong><br>
            Let the employer know you have been working your way through school.</li>
          </ul>
          <div align="center">
            <hr size="2" width="100%" align="center">
          </div>
          <p>When  you explain your GPA honestly and with confidence, the&nbsp;employer is likely  to share in your confidence as a candidate.   Place your GPA in the most positive context  possible. </p>
          <p>            Keep  in mind that each employer places different emphasis and weight on GPA.  For some employers, a minimum GPA requirement  is firm and non-negotiable.  For others,  it is preferred and may be more flexible.   When you come close to meeting a GPA requirement or you have other  strengths of interest to the employer,<em><strong> apply for the opportunity!</strong></em>  </p>
          <p>            Your <a href="../../about/staff.php">career  counselor</a><a href="../../about/staff.php"></a> can help you present and talk about your GPA in the best light. </p>
